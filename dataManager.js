let currentLeague = 'en_1';
export let collectedStickers = {};
export let duplicateStickers = {};

function initializeCollections() {
  Object.keys(leagues).forEach(league => {
    collectedStickers[league] = new Set();
    duplicateStickers[league] = {};
  });
}

// Switch to a different league
function switchLeague(leagueKey) {
  currentLeague = leagueKey;
  document.querySelectorAll('.league-button').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.league === leagueKey);
  });
  document.querySelector('.album-title').textContent = `${leagues[leagueKey].name} Sticker 2025 Album`;
  updateDuplicates();
  initializeAlbum();
  resetGame();
  return leagues[leagueKey];
}

function addSticker(teamId) {
  if (collectedStickers[currentLeague].has(teamId)) {
    duplicateStickers[currentLeague][teamId] = (duplicateStickers[currentLeague][teamId] || 0) + 1;
    return 'duplicate';
  } else {
    collectedStickers[currentLeague].add(teamId);
    return 'new';
  }
}

function getCollectedStickers() {
  return collectedStickers[currentLeague];
}

function getDuplicates() {
  return duplicateStickers[currentLeague];
}

export {
  leagues,
  currentLeague,
  initializeCollections,
  switchLeague,
  addSticker,
  getCollectedStickers,
  getDuplicates
};
